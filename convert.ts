const vulgarToDecimalRegex = /[¼½¾⅐⅑⅒⅓⅔⅕⅖⅗⅘⅙⅚⅛⅜⅝⅞]/g;

function toDecimal(match) {
  const values = VULGAR_MAP.get(match)
  return values[0].toString()
}

function convertVulgarToDecimal(text) {
  return text.replace(vulgarToDecimalRegex, toDecimal)
}

